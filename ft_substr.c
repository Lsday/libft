#include"libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*ptr;
	size_t	i;

	i = 0;
	ptr = malloc(sizeof(char) * len + 1);
	if (!ptr)
		return (NULL);
	if (start >= ft_strlen(s))
	{
		*ptr = '\0';
		return (ptr);
	}
	while (i < len)
	{
		ptr[i] = s[start + i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
